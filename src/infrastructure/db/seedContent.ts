async function insertNode(pool: Pool, parentId: number | null, title: string, sortOrder: number): Promise<number> {
  const result = await pool.query<{ id: number }>(
    'INSERT INTO nodes (parent_id, title, sort_order) VALUES ($1, $2, $3) RETURNING id',
    [parentId, title, sortOrder],
  );
  return result.rows[0].id;
}

async function insertPage(pool: Pool, nodeId: number, title: string, description: string, sortOrder: number): Promise<void> {
  await pool.query('INSERT INTO pages (node_id, title, description, sort_order) VALUES ($1, $2, $3, $4)', [
    nodeId,
    title,
    description,
    sortOrder,
  ]);
}

